Aes::Aes(const std::uint8_t* key, std::size_t key_len) {
  if (key_len != 16 && key_len != 24 && key_len != 32)
    throw std::invalid_argument("Aes: key length must be 16, 24 or 32 bytes");

  int nk = static_cast<int>(key_len / 4);
  rounds_ = nk + 6;
  int total = 16 * (rounds_ + 1);  // bytes of expanded key

  std::memcpy(round_keys_, key, key_len);
  int rcon_iter = 1;
  std::uint8_t t[4];
  for (int i = static_cast<int>(key_len); i < total; i += 4) {
    std::memcpy(t, round_keys_ + i - 4, 4);
    if (i % static_cast<int>(key_len) == 0) {
      std::uint8_t tmp = t[0];  // RotWord
      t[0] = t[1];
      t[1] = t[2];
      t[2] = t[3];
      t[3] = tmp;
      for (int j = 0; j < 4; ++j) t[j] = kSbox[t[j]];  // SubWord
      t[0] ^= kRcon[rcon_iter++];
    } else if (key_len == 32 && i % static_cast<int>(key_len) == 16) {
      for (int j = 0; j < 4; ++j) t[j] = kSbox[t[j]];
    }
    for (int j = 0; j < 4; ++j)
      round_keys_[i + j] = round_keys_[i + j - static_cast<int>(key_len)] ^ t[j];
  }
}

Aes::~Aes() {
  volatile std::uint8_t* p = round_keys_;
  for (std::size_t i = 0; i < sizeof(round_keys_); ++i) p[i] = 0;
}

void Aes::encrypt_block(const std::uint8_t in[kBlockSize],
                        std::uint8_t out[kBlockSize]) const {
  std::uint8_t s[16];
  std::memcpy(s, in, 16);

  auto add_round_key = [&](int round) {
    const std::uint8_t* rk = round_keys_ + 16 * round;
    for (int i = 0; i < 16; ++i) s[i] ^= rk[i];
  };

  add_round_key(0);

  for (int round = 1; round < rounds_; ++round) {
    for (int i = 0; i < 16; ++i) s[i] = kSbox[s[i]];  // SubBytes

    // ShiftRows (state is column-major: byte index = 4*col + row).
    std::uint8_t tmp;
    tmp = s[1]; s[1] = s[5]; s[5] = s[9]; s[9] = s[13]; s[13] = tmp;
    std::swap(s[2], s[10]); std::swap(s[6], s[14]);
    tmp = s[15]; s[15] = s[11]; s[11] = s[7]; s[7] = s[3]; s[3] = tmp;

    // MixColumns
    for (int c = 0; c < 4; ++c) {
      std::uint8_t a0 = s[4 * c], a1 = s[4 * c + 1], a2 = s[4 * c + 2],
                   a3 = s[4 * c + 3];
      s[4 * c] = xtime(a0) ^ (xtime(a1) ^ a1) ^ a2 ^ a3;
      s[4 * c + 1] = a0 ^ xtime(a1) ^ (xtime(a2) ^ a2) ^ a3;
      s[4 * c + 2] = a0 ^ a1 ^ xtime(a2) ^ (xtime(a3) ^ a3);
      s[4 * c + 3] = (xtime(a0) ^ a0) ^ a1 ^ a2 ^ xtime(a3);
    }

    add_round_key(round);
  }

  // Final round (no MixColumns).
  for (int i = 0; i < 16; ++i) s[i] = kSbox[s[i]];
  std::uint8_t tmp;
  tmp = s[1]; s[1] = s[5]; s[5] = s[9]; s[9] = s[13]; s[13] = tmp;
  std::swap(s[2], s[10]); std::swap(s[6], s[14]);
  tmp = s[15]; s[15] = s[11]; s[11] = s[7]; s[7] = s[3]; s[3] = tmp;
  add_round_key(rounds_);

  std::memcpy(out, s, 16);
}

void Aes::ctr_xor(const std::uint8_t* in, std::uint8_t* out, std::size_t len,
                  std::uint8_t counter[kBlockSize]) const {
  std::uint8_t keystream[kBlockSize];
  std::size_t offset = 0;
  while (offset < len) {
    encrypt_block(counter, keystream);
    std::size_t take = std::min(kBlockSize, len - offset);
    for (std::size_t i = 0; i < take; ++i)
      out[offset + i] = in[offset + i] ^ keystream[i];
    offset += take;
    // Increment the 128-bit big-endian counter.
    for (int i = static_cast<int>(kBlockSize) - 1; i >= 0; --i)
      if (++counter[i] != 0) break;
  }
}

