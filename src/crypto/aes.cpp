const std::uint8_t kRcon[15] = {0x00, 0x01, 0x02, 0x04, 0x08, 0x10, 0x20, 0x40,
                                0x80, 0x1b, 0x36, 0x6c, 0xd8, 0xab, 0x4d};

inline std::uint8_t xtime(std::uint8_t x) {
  return static_cast<std::uint8_t>((x << 1) ^ ((x >> 7) * 0x1b));
}

